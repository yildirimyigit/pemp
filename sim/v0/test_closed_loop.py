def build_arm_delta_grid() -> np.ndarray:
    candidates = []
    for d0 in (-0.8, -0.4, 0.0, 0.4, 0.8):
        for d1 in (-0.5, 0.0, 0.5):
            for d2 in (-1.0, -0.6, -0.3, 0.0, 0.3, 0.6):
                for d3 in (0.0, 0.4, 0.8, 1.2):
                    candidates.append((d0, d1, d2, d3))
    return np.array(candidates, dtype=np.float32)

ARM_DELTA_CANDIDATES = build_arm_delta_grid()

def get_task_sites(env: gym.Env) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    base_env = env.unwrapped
    palm = base_env.data.site_xpos[base_env.S_grasp_site_id].copy()
    tool = base_env.data.site_xpos[base_env.tool_site_id].copy()
    nail = base_env.data.site_xpos[base_env.target_obj_site_id].copy()
    return palm, tool, nail

def normalized_action_to_ctrl(base_env, action: np.ndarray) -> np.ndarray:
    action = np.clip(action, -1.0, 1.0)
    return base_env.act_mean + action * base_env.act_rng

def save_sim_state(base_env) -> tuple[np.ndarray, np.ndarray, np.ndarray, float]:
    return (
        base_env.data.qpos.copy(),
        base_env.data.qvel.copy(),
        base_env.data.ctrl.copy(),
        float(base_env.data.time),
    )

def restore_sim_state(base_env, state: tuple[np.ndarray, np.ndarray, np.ndarray, float]) -> None:
    qpos, qvel, ctrl, sim_time = state
    base_env.set_state(qpos, qvel)
    base_env.data.ctrl[:] = ctrl
    base_env.data.time = sim_time

def simulate_unrendered_step(base_env, action: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    base_env.do_simulation(normalized_action_to_ctrl(base_env, action), base_env.frame_skip)
    tool = base_env.data.site_xpos[base_env.tool_site_id].copy()
    nail = base_env.data.site_xpos[base_env.target_obj_site_id].copy()
    return tool, nail

def select_closed_loop_strike_action(
    env: gym.Env,
    base_action: np.ndarray,
    reference_offsets: list[np.ndarray],
    cycle_step: int,
    lookahead_steps: int,
) -> np.ndarray:
    base_env = env.unwrapped
    initial_state = save_sim_state(base_env)
    best_score = np.inf
    best_action = base_action.copy()
    horizon = max(1, min(lookahead_steps, STRIKE_CYCLE_STEPS - cycle_step))

    for arm_delta in ARM_DELTA_CANDIDATES:
        action = base_action.copy()
        action[:4] = np.clip(action[:4] + arm_delta, -1.0, 1.0)
        score = 0.0
        closest_impact_distance = np.inf

        for horizon_step in range(horizon):
            if horizon_step == 0:
                rollout_action = action
            else:
                template_step = min(cycle_step + horizon_step, STRIKE_CYCLE_STEPS - 1)
                rollout_action = STRIKE_TEMPLATE_ACTIONS[template_step]

            tool, nail = simulate_unrendered_step(base_env, rollout_action)
            reference_step = min(cycle_step + horizon_step, len(reference_offsets) - 1)
            offset_error = (tool - nail) - reference_offsets[reference_step]
            score += (horizon_step + 1) * np.linalg.norm(OFFSET_ERROR_WEIGHTS * offset_error)

            if reference_step >= IMPACT_PHASE_STEP:
                closest_impact_distance = min(closest_impact_distance, np.linalg.norm(tool - nail))

        if cycle_step >= 10:
            score += 0.7 * closest_impact_distance

        restore_sim_state(base_env, initial_state)
        if score < best_score:
            best_score = score
            best_action = action

    restore_sim_state(base_env, initial_state)
    return best_action

def run_episode(
    seed: int,
    render: bool,
    sleep_s: float,
    verbose: bool,
    closed_loop: bool,
    lookahead_steps: int,
) -> bool:
    env = gym.make(ENV_ID, render_mode="human" if render else None, max_episode_steps=500)
    obs, _ = env.reset(seed=seed)

    success = False
    strike_reference_offsets: list[np.ndarray] = []
    
    executed_actions = []
    
    # Let's define the stable grasp explicitly by taking the hand actions from step 49 and slightly tightening it:
    # Actually, we can just use the exact sequence without messing with the fingers except maybe during the settle.
    # But if it falls out during settle... wait, what if we just use the closed-loop controller
    # during the retract AND settle phases to keep the tool-nail offset constant?
    # No, closed-loop only searches over arm_delta. If it falls out, it means the grasp is weak.
    
    # We will enforce a tight grasp during the whole strike phase.
    tight_grasp = SCRIPTED_ACTIONS[49, 4:].copy()
    
    def step_env(action, step_idx):
        nonlocal success
        
        # Override hand to tightly hold after pickup
        if step_idx >= 50:
            action[4:] = tight_grasp
            
        executed_actions.append(action.copy())
        _, reward, term, trunc, info = env.step(action)
        success = success or bool(info.get("success", False))
        if render and sleep_s > 0:
            time.sleep(sleep_s)
        return term or trunc

    step_counter = 0

    # PHASE 0: Pickup
    for action in PICKUP_ACTIONS:
        if step_env(action.copy(), step_counter): break
        step_counter += 1

    for strike_index in range(NUM_STRIKES):
        # Retract
        for i in range(10):
            base_action = STRIKE_TEMPLATE_ACTIONS[i].copy()
            # The closed-loop controller uses the original fingers of the template, so let's override them for its search:
            base_action[4:] = tight_grasp
            action = base_action.copy()
            if closed_loop and strike_index > 0 and len(strike_reference_offsets) == STRIKE_CYCLE_STEPS:
                action = select_closed_loop_strike_action(
                    env, base_action, strike_reference_offsets, i, lookahead_steps
                )
            if step_env(action, step_counter): break
            step_counter += 1
            if strike_index == 0:
                palm, tool, nail = get_task_sites(env)
                strike_reference_offsets.append(tool - nail)
                
        # Settle
        settle_action = executed_actions[-1].copy()
        for _ in range(SETTLE_STEPS):
            if step_env(settle_action, step_counter): break
            step_counter += 1
            # we don't append to reference offsets during settle!
            
        # Impact
        for i in range(10, 20):
            base_action = STRIKE_TEMPLATE_ACTIONS[i].copy()
            base_action[4:] = tight_grasp
            action = base_action.copy()
            if closed_loop and strike_index > 0 and len(strike_reference_offsets) == STRIKE_CYCLE_STEPS:
                action = select_closed_loop_strike_action(
                    env, base_action, strike_reference_offsets, i, lookahead_steps
                )
            if step_env(action, step_counter): break
            step_counter += 1
            if strike_index == 0:
                palm, tool, nail = get_task_sites(env)
                strike_reference_offsets.append(tool - nail)

    env.close()
    print(f"finished seed={seed} success={success}")
    return success

